CREATE TABLE IF NOT EXISTS pensatta_record (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    score INTEGER NOT NULL,
    time INTEGER NOT NULL,
    date TIMESTAMP(0) NOT NULL,
    exercise_id BIGINT NOT NULL,
    user_id BIGINT NOT NULL
);

CREATE TABLE IF NOT EXISTS pensatta_calification (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    average_score INTEGER NOT NULL DEFAULT 0,
    average_time INTEGER NOT NULL DEFAULT 0,
    user_id BIGINT NOT NULL
);

CREATE TABLE IF NOT EXISTS pensatta_exercise (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    topic INTEGER NOT NULL,
    dimension INTEGER NOT NULL,
    appropriation INTEGER NOT NULL,
    skills VARCHAR(64) NOT NULL
);
